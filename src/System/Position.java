package System;

public class Position {
    private double lat;
    private double lon;

    public void Position(double latitude, double longitude) {
        this.lat = latitude;
        this.lon = longitude;
    }

    public double getLat() {
        return lat;
    }

    public double getLon() {
        return lon;
    }
}
